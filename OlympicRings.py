# program to draw olympic rings with turtle
from turtle import *
penup()
pensize(25)
pencolor('blue')
goto(-400, 100)
pendown()
circle(100)
penup()
goto(-275, -25)
pencolor('yellow')
pendown()
circle(100)
penup()
goto(-150, 100)
pencolor('black')
pendown()
circle(100)
penup()
goto(-50, -25)
pencolor('green')
pendown()
circle(100)
penup()
goto(75, 100)
pencolor('red')
pendown()
circle(100)
done()